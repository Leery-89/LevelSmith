"""
glb_to_fbx.py
将 GLB 文件转换为 UE5 可用的 FBX ASCII 7.4 格式。

坐标系转换:
  GLTF  : Y-up, 右手系 (X=右, Y=上, Z=朝观察者)
  UE5   : Z-up, 左手系 (X=前, Y=右, Z=上)
  映射  : UE5_X=-GLTF_Z, UE5_Y=GLTF_X, UE5_Z=GLTF_Y
  比例  : ×100 (米 → 厘米)
  绕组  : 取反 Z 轴 → 镜像 → 翻转三角形绕组 (v0,v1,v2) → (v0,v2,v1)

用法:
    python glb_to_fbx.py level_test.glb
    python glb_to_fbx.py level_test.glb --out level_test.fbx
"""

import argparse
import io
import sys
import time
from pathlib import Path

# Fix Chinese print on Windows cp1252 consoles
