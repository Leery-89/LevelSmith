    W(f"                a: {_fmt_floats(all_normals, 6)}")
    W("            }")
    W("        }")

    # Vertex Colors (ByPolygonVertex, Direct — no ColorIndex needed)
    W("        LayerElementColor: 0 {")
    W("            Version: 101")
    W('            Name: "Col"')
    W('            MappingInformationType: "ByPolygonVertex"')
    W('            ReferenceInformationType: "Direct"')
    W(f"            Colors: *{n_pverts * 4} {{")
